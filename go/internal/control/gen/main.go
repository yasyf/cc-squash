const wireBuildPrefix = "com.yasyf.cc-squash.control/"

var errGeneratedOutputStale = errors.New("control: generated wire schema is stale")

func main() {
	check := flag.Bool("check", false, "verify generated output")
	flag.Parse()
	path := filepath.Join(moduleRoot(), "internal", "control", "protocol_gen.go")
	if err := run(*check, wireSpec, path); errors.Is(err, errGeneratedOutputStale) {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	} else if err != nil {
		panic(err)
	}
}

func run(check bool, definition, path string) error {
	generated, _, err := generate(definition)
	if err != nil {
		return err
	}
	if check {
		current, err := os.ReadFile(path)
		if err != nil || !bytes.Equal(current, generated) {
			return errGeneratedOutputStale
		}
		return nil
	}
	return os.WriteFile(path, generated, 0o644)
}

func generate(definition string) ([]byte, string, error) {
	canonical, insertionOffset, err := canonicalWireSpec(definition)
	if err != nil {
		return nil, "", err
	}
	digest := sha256.Sum256(canonical)
	fingerprint := hex.EncodeToString(digest[:])
	identity := []byte(fmt.Sprintf(`

const (
	WireSchemaFingerprint = %q
	WireBuild = %q + WireSchemaFingerprint + "/v1"
)
`, fingerprint, wireBuildPrefix))
	source := make([]byte, 0, len(canonical)+len(identity)+64)
	source = append(source, "// Code generated by internal/control/gen; DO NOT EDIT.\n\n"...)
	source = append(source, canonical[:insertionOffset]...)
	source = append(source, identity...)
	source = append(source, canonical[insertionOffset:]...)
	generated, err := format.Source(source)
	if err != nil {
		return nil, "", err
	}
	return generated, fingerprint, nil
}

func canonicalWireSpec(definition string) ([]byte, int, error) {
	const packageClause = "package control\n\n"
	source, err := format.Source([]byte(packageClause + definition))
	if err != nil {
		return nil, 0, err
	}
	files := token.NewFileSet()
	file, err := parser.ParseFile(files, "wire_spec.go", source, parser.SkipObjectResolution)
	if err != nil {
		return nil, 0, err
	}
	insertionOffset := len(packageClause)
	for _, declaration := range file.Decls {
		generic, ok := declaration.(*ast.GenDecl)
		if !ok || generic.Tok != token.IMPORT && generic.Tok != token.CONST && generic.Tok != token.TYPE {
			return nil, 0, errors.New("control: wire schema permits only import, const, and type declarations")
		}
		if generic.Tok == token.IMPORT {
			insertionOffset = files.Position(generic.End()).Offset
		}
		for _, item := range generic.Specs {
			switch spec := item.(type) {
			case *ast.TypeSpec:
				if reservedIdentity(spec.Name.Name) {
					return nil, 0, errors.New("control: generated identity cannot be declared by the wire schema")
				}
			case *ast.ValueSpec:
				for _, name := range spec.Names {
					if reservedIdentity(name.Name) {
						return nil, 0, errors.New("control: generated identity cannot be declared by the wire schema")
					}
				}
			}
		}
	}
	return source, insertionOffset, nil
}

func reservedIdentity(name string) bool {
	return name == "WireSchemaFingerprint" || name == "WireBuild"
}

func moduleRoot() string {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		panic("cannot locate generator")
	}
	return filepath.Clean(filepath.Join(filepath.Dir(file), "..", "..", ".."))
}

